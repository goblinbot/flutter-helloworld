class BigCard extends StatelessWidget {
  const BigCard({
    super.key,
    required this.pair,
  });

  final WordPair pair;

  @override
  Widget build(BuildContext context) {
    final theme = Theme.of(context);

    final style = theme.textTheme.displayMedium!.copyWith(
      color: theme.colorScheme.onPrimary,
    );

    return Card(
      color: theme.colorScheme.primary,
      child: Padding(
        padding: const EdgeInsets.all(20),
        child: Text(
          pair.asLowerCase,
          style: style,
          semanticsLabel:
              "${pair.first} ${pair.second}", // read first and second word separate for screen readers!
        ),
      ),
    );
  }
}

class FavoritePage extends StatelessWidget {
  @override
  Widget build(BuildContext context) {
    var appState = context.watch<MyAppState>();
    // var favorites = appState.favorites.map((m) => Text('${m}')).toList();
    var favorites = appState.favorites;

    if (favorites.isEmpty) {
      return Padding(
          padding: const EdgeInsets.all(20), child: Text('No favorites yet.'));
    }

    return ListView(
      children: [
        Padding(
          padding: const EdgeInsets.all(20),
          child: Text('You have ${favorites.length} favorite(s)'),
        ),
        SizedBox(height: 4),
        for (var fav in favorites)
          ListTile(
              leading: Icon(Icons.delete),
              title: Text('$fav'),
              onTap: () {
                appState.removeFavorite(fav);
              }),
      ],
    );
  }
}
